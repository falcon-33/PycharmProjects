number = int(input())
numberPlus = number + 1
numberMinus = number - 1
numberString = str(number)
numberPlusString = str(numberPlus)
numberMinusString = str(numberMinus)
print('The next number for the number', numberString,
      'is', numberPlusString, end='.')
print()
print('The previous number for the number', numberString,
      'is', numberMinusString, end='.')
