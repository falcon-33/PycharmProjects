a = {1, 2, 3, 4}
b = {1, 3, 5}
print(a | b)  # обьединение двух множеств
print(a & b)  # пересечение множеств
print(a - b)  # вывести все элементы, которые соержатся в "а" и не содержатся в "b"
print(a ^ b)  # симметрическая разность: все элементы входящие в объедениние множеств, но не входящие в их пересечение
